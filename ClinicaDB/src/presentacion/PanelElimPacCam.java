    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        String[] usuariosCam = controlCam.listarUsuCamp(misCamp[cmbCam.getSelectedIndex()].getCodigoCampana());
        listPacien.setListData(usuariosCam);
    }//GEN-LAST:event_btBuscarActionPerformed

    private void btEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEliminarActionPerformed
        String docum = listPacien.getSelectedValue().toString();
        int resultado = controlCam.eliminarPacCamp(docum, misCamp[cmbCam.getSelectedIndex()].getCodigoCampana());
        if (resultado > 0) {
                JOptionPane.showMessageDialog(this, "Paciente eliminado", "Mensaje de confirmacion", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Se produjo un error, confirme los datos o contacte al administrador", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btEliminarActionPerformed

 

